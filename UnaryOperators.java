public class UnaryOperators {
    public static void main(String[] args) {
        int a = 20;
        System.out.println(a++); // 20
        System.out.println(++a); // 22
        System.out.println(a--); // 22
        System.out.println(--a); // 20
        System.out.println("-------");
        System.out.println(a);   // 20
    }
}
